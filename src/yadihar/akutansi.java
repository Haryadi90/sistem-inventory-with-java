    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
          if(Kode.getSelectedItem().equals("") || txtNamaBarang.getText().equals("") || txtHarga.getText().equals("")|| txtQty_Rcv.getText().equals("")|| txtPassed.getText().equals("") || txtTotal.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Silahkan Lengkapi Data", "manajament system", JOptionPane.INFORMATION_MESSAGE);
        }else{
            String no_pembayaran =  txtNo_Pembayaran.getText();
            String kode = (String) Kode.getSelectedItem();
            String NmBrng = txtNamaBarang.getText();
            String hrg = txtHarga.getText();
            String qty = txtQty_Rcv.getText();
            String passed = txtPassed.getText();
            String total = txtTotal.getText();
            try{
                Connection c = konfig.configDB();
                String sql="Insert into akutansi values (?,?,?,?,?,?,?)";
                PreparedStatement p=(PreparedStatement)c.prepareStatement(sql);
                p.setString(1,no_pembayaran);
                p.setString(2,kode);
                p.setString(3,NmBrng);
                p.setString(4,hrg);
                p.setString(5,qty );
                p.setString(6,passed);
                p.setString(7,total);
                p.executeUpdate();
                p.close();
            
            
            
            }catch(SQLException e){
                System.out.println(e);
            }finally{
                tampilkan_data();
                JOptionPane.showMessageDialog(this,"Data Telah Tersimpan");
               
               kosong();
                }
        } 
    }//GEN-LAST:event_simpanActionPerformed

