    private void txtPassedKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPassedKeyPressed
        // TODO add your handling code here:
         if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (txtQty_Rcv.getText().equals("") || txtHarga.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Silahkan Lengkapi Data", "TK. Sinar Abadi", JOptionPane.INFORMATION_MESSAGE);
            }else{
                String a = txtQty_Rcv.getText();
                int aa = Integer.parseInt(a);
                String b = txtQty_Rcv.getText();
                int bb = Integer.parseInt(b);
                if(aa > bb){
                    JOptionPane.showMessageDialog(null, "Jumlah beli melebihi Stok yang Ada " + bb, "TK. Abadi Jaya", JOptionPane.INFORMATION_MESSAGE);
                    txtQty_Rcv.setText("");
                }else{  //mengalikan  harga dengan Qty kedatangan
                    int angka1,angka2,hasil;
                    angka1 = Integer.parseInt(txtHarga.getText().toString());
                    angka2 = Integer.parseInt(txtQty_Rcv.getText().toString());
                    hasil = angka1 * angka2;
                    txtTotal.setText(Integer.toString(hasil));
                    tampilkan_data();
        
            }
          }
        }
    }//GEN-LAST:event_txtPassedKeyPressed

